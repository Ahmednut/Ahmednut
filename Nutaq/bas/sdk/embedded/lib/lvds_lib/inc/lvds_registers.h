#ifndef LVDS_REGISTERS__
#define LVDS_REGISTERS__

// INFO
typedef struct S_lvds_coreversid {
#ifdef _BIG_ENDIAN
    unsigned coreid:16, coreversion:16;
#else
    unsigned coreversion:16, coreid:16;
#endif
} lvds_coreversid;

// CONTROL
typedef struct S_lvds_ctrl{
#ifdef _BIG_ENDIAN
	unsigned rsvd:31, CoreReset:1;
#else
	unsigned CoreReset:1, rsvd:31;
#endif
} lvds_ctrl;

typedef struct S_lvds_core {
    lvds_coreversid coreversid;
    lvds_ctrl control;
    unsigned value;
    unsigned outputenable;
} lvds_core;

#endif

