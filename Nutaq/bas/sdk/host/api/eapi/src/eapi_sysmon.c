#include "cce.h"
#include "eapi_sysmon.h"
#include "sysmon_def.h"
#include "eapi_serialize.h"

Result Sysmon_Read_send(connection_state * state, Sysmon_eType_t type, float * pValue)
{    
    unsigned int value;
    float * valuef = (float *)&value;
    cmd_send(state, SYSMON_READ);
    uint_send(state, type);
    
    value = uint_recv(state);
    
    *pValue = *valuef;
    
    return result_recv(state);
}

